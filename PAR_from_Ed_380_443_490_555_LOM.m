% estimates.
%
% PAR = PAR_from_Ed_380_443_490_555_LOM(Ed)
% Input arguments:
%   Ed · Nx4 matrix: downwelling irradiance spectrum
% Output arguments:
%   PAR    · Nx1 matrix: Estimated PAR
% where N is the number of samples.
%
% This function is self-contained.
% Jaime Pitarch, CNR-ISMAR, 27-Sep-2024.

% Example run:
% my_Ed =[0.00089262    0.0069923     0.018696    0.0023898
%        1.65e-06   9.9975e-05   0.00075004   5.8526e-06
%        0.058626      0.14587      0.14568     0.035567];
%    PAR = PAR_from_Ed_380_443_490_555_LOM(my_Ed);

% ===== NEURAL NETWORK CONSTANTS =====

% Input 1
x1_step1.xoffset = [-22.6422533091432;-19.5694508081377;-7.80877488738832;-21.2406406116215];
x1_step1.gain = [0.0869916582169695;0.0986990164692655;0.23427495150328;0.0912216794081883];
x1_step1.ymin = -1;

% Layer 1
b1 = [-7.0620459107811166533;2.6662318901007600047;2.3512166890411760889;2.7526081360171659185;-2.3063563918212048875;-1.0188811217515849616;0.56049704628021324027;-1.9431002431700723498;2.250426808036069648;2.4803400649875961825];
