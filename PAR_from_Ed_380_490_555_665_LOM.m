%         0.058626      0.14568     0.035567   6.5366e-05];
%    PAR = PAR_from_Ed_380_490_555_665_LOM(my_Ed);

% ===== NEURAL NETWORK CONSTANTS =====

% Input 1
x1_step1.xoffset = [-22.6422533091432;-7.80877488738832;-21.2406406116215;-22.1732499446457];
x1_step1.gain = [0.0869916582169695;0.23427495150328;0.0912216794081883;0.0888559936201479];
